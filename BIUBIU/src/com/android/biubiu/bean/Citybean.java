package com.android.biubiu.bean;

public class Citybean {
	private String id;
	private String privance;
	private String privance_num;
	private String city;
	private String city_num;
	private String town;
	private String town_num;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPrivance() {
		return privance;
	}

	public void setPrivance(String privance) {
		this.privance = privance;
	}

	public String getPrivance_num() {
		return privance_num;
	}

	public void setPrivance_num(String privance_num) {
		this.privance_num = privance_num;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCity_num() {
		return city_num;
	}

	public void setCity_num(String city_num) {
		this.city_num = city_num;
	}

	public String getTown() {
		return town;
	}

	public void setTown(String town) {
		this.town = town;
	}

	public String getTown_num() {
		return town_num;
	}

	public void setTown_num(String town_num) {
		this.town_num = town_num;
	}

}
